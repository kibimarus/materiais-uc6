paises = ["Brasil", "Grécia", "Porto-rico", "Haiti", "Alemanha", "Cánada", "Suécia", "Singapura", "Suiça", "México", "Bangladesh"]
#? o loop for é muito utilizado em 'sequências' onde vetor é uma das sequencias
#? Na traduçaõ ele se chama "para" 
#? o loop funciona muito bem com vetores 
#? Aqui usamos o operador 'in' para outra coisa 
#? 'paises' é o vetor 
